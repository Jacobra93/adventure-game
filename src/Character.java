public class Character {
    public String name;
    public double level;
    public double magicLevel;
    public double stealthLevel;
    public double strengthLevel;

    public Character (String name) {
        this.name = name;
        this.level = 1;
        this.magicLevel = 1;
        this.stealthLevel = 1;
        this.strengthLevel = 1;
    }

}
